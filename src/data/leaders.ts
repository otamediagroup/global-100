export function generateSlug(name: string): string {
  return name
    .toLowerCase()
    .replace(/\s+/g, '-')
    .replace(/[^\w-]/g, '');
}

export function getLeaderBySlug(slug: string): Leader | undefined {
  return leaders.find((leader) => leader.slug === slug);
}

export function getLeadersByTier(tier: 'vanguard' | 'momentum'): Leader[] {
  return leaders.filter((leader) => leader.tier === tier);
}

export function getLeadersBySector(sector: string): Leader[] {
  return leaders.filter((leader) => leader.sector === sector);
}

export function getLeadersByCountry(country: string): Leader[] {
  return leaders.filter((leader) => leader.country === country);
}

